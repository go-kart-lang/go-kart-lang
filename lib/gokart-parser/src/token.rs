#[derive(Debug, PartialEq)]
pub enum Token<'a> {
    LBrace,
    RBrace,
    LParen,
    RParen,
    LBracket,
    RBracket,
    Comma,
    Semicolon,
    Str(&'a str),
    Int(i64),
    Double(f64), // todo
    Let,
    Letrec,
    Data,
    In,
    If,
    Then,
    Else,
    Case,
    Of,
    Infixl,
    Infixr,
    As,
    Udent(&'a str),
    Ident(&'a str),
    Eq,
    Backslash,
    Pipe,
    Arrow,
    Opr(&'a str),
}
